public class Client {
    private String document;
    private String name;
    private String firstLastName;
    private String lastLastName;
    private String email;
    private String phone;

    public String getDocument() {
        return document;
    }

    public void setDocument(String document) {
        this.document = document;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFirstLastName() {
        return firstLastName;
    }

    public void setFirstLastName(String firsLastName) {
        this.firstLastName = firsLastName;
    }

    public String getLastLastName() {
        return lastLastName;
    }

    public void setLastLastName(String lastLastName) {
        this.lastLastName = lastLastName;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
    
    
}
