public class Provider {
    private String NIT;
    private String nameProvide;
    private String adressProvide;
    private String phoneProvide;
    private String emailProvide;
    private int type;

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    
    public String getNIT() {
        return NIT;
    }

    public void setNIT(String NIT) {
        this.NIT = NIT;
    }

    public String getNameProvide() {
        return nameProvide;
    }

    public void setNameProvide(String nameProvide) {
        this.nameProvide = nameProvide;
    }

    public String getAdressProvide() {
        return adressProvide;
    }

    public void setAdressProvide(String adressProvide) {
        this.adressProvide = adressProvide;
    }

    public String getPhoneProvide() {
        return phoneProvide;
    }

    public void setPhoneProvide(String phoneProvide) {
        this.phoneProvide = phoneProvide;
    }

    public String getEmailProvide() {
        return emailProvide;
    }

    public void setEmailProvide(String emailProvide) {
        this.emailProvide = emailProvide;
    }
    
    
}
