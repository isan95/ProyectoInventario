    private void jButtonSaveUserEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveUserEditActionPerformed
        QueryUser qpu = new QueryUser();
        User usr = new User();
        String typeUserupd = new String(this.jComboBoxUserUpd.getSelectedItem().toString());
        int auxTypeUsr=0;
        String passUsrUpd = new String(this.jPasswordField1.getPassword());
        if(typeUserupd.equals("Administrador")){
            auxTypeUsr=1;
        }
        else{
            if(typeUserupd.equals("Usuario")){
                auxTypeUsr=2;
            }
        }
        usr.setNick(this.jTextNickUpdate.getText());
        usr.setName(this.jTextNameUpdate.getText());
        usr.setpApellido(this.jTextFLasNameUpd.getText());
        usr.setsApellido(this.jTextLLastNameUpd.getText());
        usr.setRol(auxTypeUsr);
        usr.setPassword(passUsrUpd);
        if(qpu.updateUser(usr)){
            JOptionPane.showMessageDialog(this,"Usuario actualizado");
            this.dispose();
        }
        else{
              JOptionPane.showMessageDialog(this,"Problemas al actualizar el usuario");
            this.dispose();
        }
    }//GEN-LAST:event_jButtonSaveUserEditActionPerformed

