    private void jButtonStartSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonStartSesionActionPerformed
       QueryUser qu = new QueryUser();
       User usr = new User();
       String pass = new String(jPasswordField.getPassword());
       int rol=2;
       if(jComboBoxRol.getSelectedItem().toString()=="Administrador"){
           rol=1;
       }
       if(!jTextFieldUser.getText().equals("") && !pass.equals("")){
           usr.setNick(jTextFieldUser.getText());
           usr.setPassword(pass);
           usr.setRol(rol);
           if(qu.login(usr)){
               this.dispose();
               frmHome = new Home();
               frmHome.setVisible(true);
               frmHome.setLocationRelativeTo(null);
           }
           else{
           JOptionPane.showMessageDialog(null,"Datos incorrectos");
           }
       }
    }//GEN-LAST:event_jButtonStartSesionActionPerformed

