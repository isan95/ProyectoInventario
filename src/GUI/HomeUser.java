    private boolean checkMail(String mail) {
        String emailPattern = "^[_a-z0-9-]+(\\.[_a-z0-9-]+)*@"
                + "[a-z0-9-]+(\\.[a-z0-9-]+)*(\\.[a-z]{2,4})$";
        Pattern pattern = Pattern.compile(emailPattern);
        String email = mail;
        boolean valide = false;
        if (mail != null) {
            Matcher matcher = pattern.matcher(email);
            if (matcher.matches()) {
                valide = true;
            }
        }
        return valide;
    }

