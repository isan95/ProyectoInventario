    private void jTextParameterKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextParameterKeyTyped
        modelProvider = new javax.swing.table.DefaultTableModel();
        jTableProvider.setModel(modelProvider);
        modelProvider.addColumn("Codigo");
        modelProvider.addColumn("Nombre");
        modelProvider.addColumn("Descripci√≥n");
        modelProvider.addColumn("Unidad de medida");
        modelProvider.addColumn("Unidades");
        String aux = ComboSearchProvider.getSelectedItem().toString();
        String attribute = new String(aux);
        QueryProvide qp = new QueryProvide();
        try {
            java.sql.ResultSet rsp = qp.getProvideForWhere(attribute, jTextParameter.getText());
            int quantityRow = qp.getCantProvide();
            while (rsp.next()) {
                Object[] rowModel = new Object[quantityRow];
                for (int i = 0; i < quantityRow; i++) {
                    rowModel[i] = rsp.getObject(i + 1);
                }
                modelProvider.addRow(rowModel);
            }
        } catch (SQLException e) {
            System.err.print(e);
        }
        qp.closeConnection();
        
    }//GEN-LAST:event_jTextParameterKeyTyped

    private void jButtonAddProviderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddProviderActionPerformed
        int selectedRow = this.jTableProvider.getSelectedRow();
        if(selectedRow>=0){
            HomeUser.getjTextNITCC().setText(modelProvider.getValueAt(selectedRow, 0).toString());
            HomeUser.getjTextNameProvider().setText(modelProvider.getValueAt(selectedRow, 1).toString());
        }
        else{
            JOptionPane.showMessageDialog(this,"Ninguna fila seleccionada");
        }
    }//GEN-LAST:event_jButtonAddProviderActionPerformed

